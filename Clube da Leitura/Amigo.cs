namespace Clube_da_Leitura
{
    public class Amigo
    {
        public string Nome;
        public string NomeResp;
        public string Telefone;
        public string Endereço;

        public Amigo(string nome, string nomeResp, string telefone, string endereço)
        {
            Nome = nome;
            NomeResp = nomeResp;
            Telefone = telefone;
            Endereço = endereço;
        }

    }
}
